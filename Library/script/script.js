function clear(){
    document.querySelector('body').style.backgroundSize = "cover";
    document.querySelector('.table table').innerHTML = "";
}

function search() {
    document.querySelector('.table table').innerHTML = "<tr> <th>Title</th> <th>Author</th> <th>Pages</th> </tr>";
    for (var i = 0; i < libraryArray.length; i++) {
        if (!libraryArray[i].Title.charAt(0).toLowerCase().localeCompare(document.getElementById("mysearch").value.charAt(0).toLowerCase())) {
            document.querySelector('.table table').innerHTML +=
                " <tr>" +
                " <td> " + libraryArray[i].Title + "</td>" +
                " <td> " + libraryArray[i].Author + "</td>" +
                " <td> " + libraryArray[i].Pages + "</td>" +
                " </tr>";
        }
    }
}

function searchLink(alphabet) {
    document.querySelector('.table table').innerHTML = "<tr> <th>Title</th> <th>Author</th> <th>Pages</th> </tr>";
    for (var i = 0; i < libraryArray.length; i++) {
        if (!libraryArray[i].Title.charAt(0).toLowerCase().localeCompare(alphabet.toLowerCase())) {
            document.querySelector('.table table').innerHTML +=
                " <tr>" +
                " <td> " + libraryArray[i].Title + "</td>" +
                " <td> " + libraryArray[i].Author + "</td>" +
                " <td> " + libraryArray[i].Pages + "</td>" +
                " </tr>";
        }
    }
}

function addList() {
    alphabet = 'A';
    for(var j=0; j<=25; j++){
        alphaNode = document.createElement('a');
        var textnode = document.createTextNode(alphabet);
        document.querySelector(".alphaList").appendChild(alphaNode);
        alphaNode.appendChild(textnode);
        alphaNode.href = "javascript:searchLink('" + alphabet + "')";
        alphabet = String.fromCharCode(alphabet.charCodeAt() + 1);
    }
    setStyle();
}

